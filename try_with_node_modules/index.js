console.log('HELLO WORLD');

import * as THREE from "./node_modules/three";
import { TrackballControls } from "./node_modules/three/examples/js/controls/TrackballControls.js";
import { OrbitControls } from "./node_modules/three/examples/js/controls/OrbitControls.js";
import { GLTFLoader } from "./node_modules/three/examples/js/loaders/GLTFLoader.js";


const appDiv = document.getElementById("app");

const width = window.innerWidth;
const height = window.innerHeight;

const scene = new THREE.Scene();
const camera = new THREE.PerspectiveCamera(50, width / height, 0.1, 1000);

/* /------ MY GLTF EXPERIMENT ------/ */
const loader = new GLTFLoader();

loader.load("./assets/parking_lot_uf.glb", (gltf) => {
    console.log('gltf loaded', gltf);
    scene.add(gltf.scene);
  }, undefined, (error) => {
    console.error(error);
  }
);

/* /------ MY GLTF EXPERIMENT ------/ */

const renderer = new THREE.WebGLRenderer();
renderer.setSize(width, height);
console.log('appending app to app div', appDiv);
appDiv?.appendChild(renderer.domElement);

const geometry = new THREE.BoxGeometry(1, 1, 1);
const material = new THREE.MeshNormalMaterial();
const cube = new THREE.Mesh(geometry, material);
scene.add(cube);

camera.position.z = 5;

const controls = new OrbitControls(camera, renderer.domElement);

function animate() {
  requestAnimationFrame(animate);

  renderer.render(scene, camera);
}

animate();
